export default function EmployerSidebar() {
  const pathname = usePathname();

  const isActive = (href: string) => {
    if (href === '/employer/dashboard') {
      return pathname === '/employer/dashboard';
    }
    if (href === '/employer/jobs') {
      return pathname === '/employer/jobs' || (pathname.startsWith('/employer/jobs/') && !pathname.includes('/new'));
    }
    return pathname.startsWith(href);
  };

  return (
    <aside className="w-64 bg-zinc-900 border-r border-zinc-800 min-h-[calc(100vh-80px)] hidden lg:block">
      <nav className="p-4 space-y-1">
        {navItems.map((item) => (
          <Link
            key={item.href}
            href={item.href}
            className={`flex items-center gap-3 px-4 py-3 rounded-xl transition-all ${
              isActive(item.href)
                ? 'bg-[#dc2626] text-white'
                : 'text-zinc-400 hover:text-white hover:bg-zinc-800'
            }`}
          >
            {item.icon}
            <span className="font-medium">{item.label}</span>
          </Link>
        ))}
      </nav>
    </aside>
  );
}
